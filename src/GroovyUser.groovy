class GroovyUser {
    String firstName;
    String lastName;
    int age;
    String address;
    GroovyUser(String firstName,String lastName,int age,String address){
        this.address=address;
        this.age=age;
        this.firstName=firstName;
        this.lastName=lastName;
    }
}
